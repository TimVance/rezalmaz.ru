    <h2 class="h2_bg">Технология: алмазное (сверление) бурение бетона</h2>
<p>Технология алмазного бурения бетона заключается в следующем:</p>
<ul>
<li>на обрабатываемой поверхности закрепляется бурильный агрегат;</li>
<li>на самом оборудовании выставляются необходимые значения, при которых отверстия в бетоне удается выполнить с наибольшей точностью;</li>
<li>рабочая часть коронки оснащается алмазными сегментами;</li>
<li>далее машина запускается в работу — сверление железобетона, а также конструкций из других твердых материалов, включая металл.</li>
</ul>
<p>Сопровождается алмазное сверление постоянной подачей воды к месту бурения, которая охлаждает режущие части инструмента и мешает образованию пыли. Использованная вода поглощается специальным пылесосом. Скорость алмазного сверления отверстий в бетоне целиком зависит от прочности пробиваемого материала.</p>
